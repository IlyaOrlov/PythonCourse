# 1. Function returns the maximum number of two variables
def max_out(a, b):
    if a > b:
        print(a)
    else:
        print(b)


# 2. The function displays the maximum number of two variables
def maximum_num(x, y):
    if x > y:
        return x
    else:
        return y


max_out(17, 19)
print(maximum_num(8, 13))
